                เพื่อให้เป็นไปตามและปฏิบัติตามกฎหมาย ระเบียบ กฎ หลักปฏิบัติ
                หรือแนวทางปฏิบัติใดๆ
                ที่ออกโดยหน่วยงานตามกฎหมายหรือที่มีอำนาจกำกับดูแล ซึ่งบริษัทฯ
                มีหน้าที่ต้องปฏิบัติตาม (รวมถึง แต่ไม่จำกัดเฉพาะ
                การตอบข้อร้องเรียนของหน่วยงานกำกับดูแล
                การเปิดเผยต่อหน่วยงานกำกับดูแล และการตรวจสอบทางบัญชี
                การตรวจสอบในเชิงลึก และการสอบสวน) และ/หรือ
                กระทำการอื่นใดที่จำเป็นในการให้บริการ บริษัทฯ
                อาจจะใช้ข้อมูลส่วนบุคคลของผู้เรียน เพื่อวัตถุประสงค์อื่นใด
                นอกไปจากที่ได้ระบุไว้ในนโยบายฉบับนี้
                โดยอยู่ภายใต้กฎหมายและระเบียบที่เกี่ยวข้อง
              </li>
            </ul>
          </div>
        </div>
        <br />
        <div align="left">
          <div class="text-h4">
            <b>
              3. บุคคลใดที่เราอาจเปิดเผยหรือโอนข้อมูลส่วนบุคคลของท่านให้
            </b>
          </div>
          <br />
          <div style="margin-top:-30px">
            ผู้เรียนยอมรับและตกลงว่า บริษัทฯ
            อาจจะมอบหมายให้ผู้ให้บริการภายนอกจัดการข้อมูลส่วนบุคคลที่เก็บรวบรวมจากผู้ประกอบการร้านค้า
            ไม่ว่าจะทั้งหมดหรือบางส่วน ในการให้บริการ โดยบริษัทฯ
            จะคำนึงถึงความจำเป็นเพื่อการบรรลุวัตถุประสงค์ในการใช้บริการ
            (“ผู้ให้บริการภายนอก”) ในกรณีเช่นว่า บริษัทฯ จะดำเนินการ
            เพื่อทำให้แน่ใจได้ว่าผู้ให้บริการภายนอกอยู่ภายใต้กฎหมายและระเบียบที่เกี่ยวข้อง
            รวมถึงประเมินคุณสมบัติ
            และความสามารถของผู้ให้บริการภายนอกอย่างเพียงพอ
            กำหนดข้อสัญญาว่าด้วยการรักษาความลับในสัญญาว่าจ้างที่ทำกับผู้ให้บริการภายนอก
            พร้อมทั้งจัดตั้งระบบการบริหารจัดการข้อมูลอย่างเหมาะสม
          </div>
        </div>
        <br />
        <div align="left">
          <div class="text-h4">
            <b>
              4. การโอนข้อมูลส่วนบุคคลของท่านไปยังต่างประเทศ
            </b>
          </div>
          <br />
          <div style="margin-top:-30px">
            เราอาจเปิดเผยหรือโอนข้อมูลส่วนบุคคลของผู้เรียนไปยังบุคคลภายนอกหรือเซิร์ฟเว่อร์ที่อยู่ในต่างประเทศซึ่งประเทศปลายทางอาจมี
            หรืออาจไม่มีระดับการคุ้มครองข้อมูลส่วนบุคคลที่เท่าเทียมกัน ทั้งนี้
            เราจะดำเนินการตามขั้นตอนและมาตรการต่างๆ
            เพื่อทำให้ผู้เรียนมั่นใจได้ว่าการโอนข้อมูลส่วนบุคคลของจะโอนอย่างปลอดภัยและบุคคลที่รับโอนข้อมูลนั้นมีระดับการคุ้มครองข้อมูลส่วนบุคคลที่เหมาะสมหรือกรณีอื่นๆ
            ตามที่กฎหมายกำหนด
            และเราจะขอความยินยอมจากท่านเพื่อการโอนข้อมูลส่วนบุคคลของท่านไปยังต่างประเทศหากจำเป็นต้องปฏิบัติตามที่กฎหมายกำหนด
          </div>
        </div>
        <br />
        <div align="left">
          <div class="text-h4">
            <b>
              5. ระยะเวลาในการที่เราเก็บข้อมูลส่วนบุคคลของท่าน
            </b>
          </div>
          <br />
          <div style="margin-top:-30px">
            เราจะเก็บรักษาข้อมูลส่วนบุคคลของผู้เรียนในระยะเวลาเท่าที่จำเป็นอย่างสมเหตุสมผล
            เพื่อให้บรรลุตามวัตถุประสงค์ที่เราได้รับข้อมูลส่วนบุคคลนั้นมา
            และเพื่อปฏิบัติตามภาระหน้าที่ทางกฎหมายและกฎข้อบังคับต่างๆ
            อย่างไรก็ตาม
            เราอาจเก็บข้อมูลส่วนบุคคลของท่านนานขึ้นหากจำเป็นตามกฎหมายที่ใช้บังคับ
          </div>
        </div>
        <br />
        <div align="left">
          <div class="text-h4">
            <b>
              6. ความปลอดภัยของข้อมูล
            </b>
          </div>
          <br />
          <div style="margin-top:-30px">
            เราตระหนักถึงความสำคัญของการรักษาความมั่นคงปลอดภัยของข้อมูลส่วนบุคคลของผู้เรียน
            เราจึงพยายามอย่างยิ่งที่จะปกป้องข้อมูลของผู้เรียนด้วยการกำหนดให้มีมาตรการในการรักษาความมั่นคงปลอดภัยของข้อมูลส่วนบุคคลของอย่างเหมาะสมและสอดคล้องกับการรักษาความลับของข้อมูลส่วนบุคคลเพื่อป้องกันการสูญหาย
            เข้าถึง ทำลาย ใช้ แปลง แก้ไขเปิดเผย
            หรือทำลายโดยไม่มีสิทธิหรือโดยไม่ชอบด้วยกฎหมาย
            โดยเราจะตรวจสอบวิธีการเก็บรวบรวมข้อมูลส่วนบุคคล
            พื้นที่ในการจัดเก็บข้อมูลและดำเนินการตามหลักปฏิบัติในการประมวลผล
            รวมถึงมาตรการความปลอดภัยทางกายภาพ
            ให้เป็นไปตามที่กำหนดในนโยบายและแนวปฏิบัติในการรักษาความมั่นคงปลอดภัยด้านเทคโนโลยีสารสนเทศ
