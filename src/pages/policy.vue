            หากผู้เรียนมีความต้องการถอนความยินยอมผู้เรียนสามารถดำเนินการได้โดยการส่งอีเมลหรือแจ้งข้อมูลให้กับเจ้าหน้าที่ด้านการปกป้องข้อมูลส่วนตัว
            และบริษัทจะดำเนินการตามคำขอในการถอนความยินยอมของผู้เรียน
            ตามกฎหมายที่เกี่ยวกับการคุ้มครองข้อมูลส่วนบุคคลและกฎหมายอื่นที่เกี่ยวข้อง
            และเงื่อนไขที่กำหนดไว้ในนโยบายความเป็นส่วนตัวฉบับนี้ อย่างไรก็ตาม
            การถอนความยินยอมของผู้เรียนอาจจะส่งผลให้เราไม่สามารถให้บริการที่เหมาะสมแก่ผู้เรียนได้อีกต่อไป
