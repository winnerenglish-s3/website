          <div style="margin-top:-30px">
            <ul style="margin-top:-0px;margin-bottom:-0px">
              <li>
                การเข้าถึง:
                ผู้เรียนอาจมีสิทธิในการขอเข้าถึงหรือขอรับสำเนาข้อมูลส่วนบุคคลที่เราเก็บรวบรวม
                ทั้งนี้
                เพื่อความปลอดภัยเราอาจขอให้ผู้เรียนพิสูจน์ตัวตนก่อนจะให้ข้อมูลตามที่ขอ
