        <div align="left">
          <div class="text-h4">
            <b>
              10. ความเป็นส่วนตัวของผู้เรียนที่ยังไม่บรรลุนิติภาวะ
            </b>
          </div>
          <br />
          <div style="margin-top:-30px">
            เราตระหนักถึงความจำเป็นที่จะต้องให้การคุ้มครองความเป็นส่วนตัวเพิ่มเติมเกี่ยวกับข้อมูลส่วนบุคคลที่บริษัทอาจเก็บรวบรวมจากผู้เรียนบนเว็บไซต์ของบริษัท
