        <div align="left">
          <div class="text-h4">
            <b>
              9. การยกเลิกการให้ข้อมูล ถอนความยินยอม ร้องขอการเข้าถึง
              หรือแก้ไขข้อมูลที่คุณให้แก่เรา
            </b>
