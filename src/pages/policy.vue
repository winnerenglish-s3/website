                ก็ได้โดยต้องแจ้งให้ทราบเป็นหนังสือล่วงหน้าอย่างน้อย 60 วัน
              </li>
              <li>
                การลบหรือทำลายข้อมูล:
                ผู้เรียนอาจมีสิทธิขอให้เราดำเนินการลบหรือทำลาย
                หรือทำให้ข้อมูลส่วนบุคคลของผู้เรียนที่เราเก็บรวบรวม
                ใช้หรือเปิดเผย
                เป็นข้อมูลที่ไม่สามารถระบุตัวบุคคลที่เป็นเจ้าของข้อมูล
