                Analytics เพื่อวิเคราะห์ว่าผู้เรียนเข้าใช้บริการของเราอย่างไร
                ข้อมูลที่รวบรวมโดยคุกกี้เกี่ยวกับการใช้บริการหรืออุปกรณ์ของผู้เรียน
                (รวมถึง IP Address)
                จะถูกส่งและเก็บไว้ในเซิร์ฟเวอร์ของกูเกิลที่ประเทศสหรัฐอเมริกา
                โดยกูเกิลจะใช้ข้อมูลนี้เพื่อประเมินการใช้บริการของเรา
                และสร้างรายงานให้กับผู้ดำเนินการเว็บไซต์ และให้บริการอื่นๆ
                ที่เกี่ยวกับกิจกรรมในเว็บไซต์และการใช้งานอินเตอร์เน็ต
                และกูเกิลอาจส่งข้อมูลนี้ให้กับบุคคลที่สามตามที่กฎหมายกำหนด
              </li>
            </ul>
